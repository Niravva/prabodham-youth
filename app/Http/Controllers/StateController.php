    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $data['country_id'] = isset($request->country_id) ? $request->country_id : 0;
        $data['states'] = State::where('country_id', $data['country_id'])->orderBy("name", "asc")->get();
        $data['selected'] = isset($request->selected) ? $request->selected : 0;
        if (isset($request->return_json) && $request->return_json == 1) {
            $html = view('state.options', $data)->render();
            return response()->json(['success' => 1, 'html' => $html]);
        }
        return view('state.options', $data);
    }

