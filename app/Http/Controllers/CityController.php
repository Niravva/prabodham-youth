    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $data['state_id'] = isset($request->state_id) ? $request->state_id : 0;
        $data['cities'] = City::where('state_id', $data['state_id'])->orderBy("name", "asc")->get();
        $data['selected'] = isset($request->selected) ? $request->selected : 0;

        if (isset($request->return_json) && $request->return_json == 1) {
            $html = view('city.options', $data)->render();
            return response()->json(['success' => 1, 'html' => $html]);
        }

        return view('city.options', $data);
    }

